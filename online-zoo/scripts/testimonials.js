function addPost(el) {
  const sliderEl = templatePost
    .querySelector(".testimonials__post")
    .cloneNode(true);

  sliderEl.querySelector(".testimonials__avatar").src = el.avatar;
  sliderEl.querySelector(".testimonials__name").textContent = el.name;
  sliderEl.querySelector(".testimonials__location").textContent = el.location;
  sliderEl.querySelector(".testimonials__text").textContext = el.text;

  return sliderEl
}
